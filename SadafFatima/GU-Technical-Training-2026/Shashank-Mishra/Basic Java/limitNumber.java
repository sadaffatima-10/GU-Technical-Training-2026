import java.util.*;
public class limitNumber {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the lower limit ");
        int ll=sc.nextInt();
        System.out.println("Enter the uppper limit");
        int ul=sc.nextInt();
        for(int i =ll ; i<=ul ; i++)
        {
            System.out.println(i);
        }
    }
    
}
